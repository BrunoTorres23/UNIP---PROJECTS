    <h1>Exemplos Práticos da Lei de Ohm</h1>
    <p>Vamos ver alguns exemplos práticos de como aplicar a Lei de Ohm.</p>
    <h2>Exemplo 1</h2>
    <p>Se a tensão em um circuito é de 12V e a resistência é de 4Ω, qual é a corrente?</p>
    <p>Usando a fórmula: I = U / R</p>
    <p>I = 12V / 4Ω = 3A</p>
    <h2>Exemplo 2</h2>
    <p>Se a corrente em um circuito é de 2A e a resistência é de 5Ω, qual é a tensão?</p>
    <p>Usando a fórmula: U = R * I</p>
    <p>U = 5Ω * 2A = 10V</p>
