    <h1>Conceitos Básicos da Lei de Ohm</h1>
    <p>A Lei de Ohm estabelece que a corrente que passa por um condutor entre dois pontos é diretamente proporcional à tensão entre esses dois pontos.</p>
    <h2>Elementos da Lei de Ohm</h2>
    <ul>
        <li><strong>Tensão (U):</strong> A diferença de potencial entre dois pontos.</li>
        <li><strong>Corrente (I):</strong> O fluxo de elétrons através de um condutor.</li>
        <li><strong>Resistência (R):</strong> A oposição ao fluxo de corrente.</li>
    </ul>
